<h1>Introduction</h1>
<p>Hurtigruten's Svelte Table is a complete table component to use in Svelte projects.</p>
<p>
	The component is designed to satisfy most use-cases that we work with daily and makes it easier to
	work with dynamic tables.
</p>

<h2>Features</h2>
<ul>
	<li>Out-of-box sorting of columns</li>
	<li>Zero effort responsiveness</li>
	<li>Dynamic data pagination</li>
	<li>Easy content mapping API</li>
	<li>Fully customizable</li>
</ul>
